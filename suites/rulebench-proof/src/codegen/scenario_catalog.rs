use rpg_core::Team;
use rpg_ir::{CheckDeclaration, RulesetMetadata, VisibilityRequirement};
use rulebench_combat::{
    AttackOutcome, DomainEvent, FinalCombatantState, ScenarioProjection, TargetLegality,
    TraceEntry, TracePhase, TraceStatus,
};
use rulebench_content::{
    Combatant, ContentDiagnostic, ContentDiagnosticSeverity, ContentValidationReport,
    RulebenchScenario,
};
use rulebench_product_content::{
    aggregated_content_validation_readouts, aggregated_ruleset_catalog_readout,
    aggregated_scenario_catalog_cases, resolve_catalog_scenario, ContentImportExampleOutcome,
    ContentValidationReadout, RulesetCatalogReadout, ScenarioCatalogSummary, ScenarioOutcomeClass,
};
use rulebench_protocol::ContentImportReadoutDto;

pub fn render_scenario_catalog() -> String {
    let summaries = aggregated_scenario_catalog_cases()
        .into_iter()
        .map(|case| case.summary)
        .collect::<Vec<_>>();
    let ruleset_catalog = aggregated_ruleset_catalog_readout();
    let mut out = format!(
        "// GENERATED by rulebench-authority/emit_scenario_catalog; schema {}@{}. Do not hand-edit.\n\n",
        rulebench_protocol::PROTOCOL_ID,
        rulebench_protocol::PROTOCOL_VERSION,
    );
    out.push_str(
        "import type { RulebenchContentImportCatalogDto, RulebenchContentValidationCatalogDto, RulebenchRulesetCatalogDto, RulebenchScenarioCatalogDto } from '@asha-rulebench/protocol';\n\n",
    );
    out.push_str(&render_ruleset_catalog(&ruleset_catalog));
    out.push('\n');
    out.push_str(&render_content_import_catalog());
    out.push('\n');
    out.push_str(&render_content_validation_catalog(
        &aggregated_content_validation_readouts(),
    ));
    out.push('\n');
    out.push_str("export const rustBackedScenarioCatalog: RulebenchScenarioCatalogDto = {\n");
    out.push_str("  summaries: [\n");
    for summary in &summaries {
        out.push_str(&render_summary(summary));
    }
    out.push_str("  ],\n");
    out.push_str("  readouts: [\n");
    for summary in &summaries {
        let resolution = resolve_catalog_scenario(&summary.id).expect("catalog summary resolves");
        let final_state = resolution
            .receipt
            .projection
            .as_ref()
            .expect("catalog case has final projection");
        out.push_str(&render_readout(
            &resolution.scenario,
            &resolution.receipt.events,
            &resolution.receipt.trace,
            final_state,
            resolution
                .receipt
                .target_legality
                .as_ref()
                .expect("catalog case has target legality"),
        ));
    }
    out.push_str("  ],\n");
    out.push_str("};\n");
    out
}

fn render_content_import_catalog() -> String {
    let mut out = String::from(
        "export const rustBackedContentImportCatalog: RulebenchContentImportCatalogDto = {\n  examples: [\n",
    );
    for example in rulebench_product_content::content_import_examples() {
        let readout = match &example.outcome {
            ContentImportExampleOutcome::Accepted(imported) => {
                ContentImportReadoutDto::accepted(&example.id, imported)
            }
            ContentImportExampleOutcome::Rejected { identity, report } => {
                ContentImportReadoutDto::rejected(&example.id, identity, report)
            }
        };
        out.push_str(&render_content_import_readout(&readout));
    }
    out.push_str("  ],\n};\n");
    out
}

fn render_content_import_readout(readout: &ContentImportReadoutDto) -> String {
    let mut out = String::from("    {\n");
    out.push_str(&format!(
        "      exampleId: {},\n",
        ts_string(&readout.example_id)
    ));
    out.push_str("      pack: {\n");
    out.push_str(&format!("        id: {},\n", ts_string(&readout.pack.id)));
    out.push_str(&format!(
        "        version: {},\n",
        ts_string(&readout.pack.version)
    ));
    match &readout.pack.fingerprint {
        Some(fingerprint) => out.push_str(&format!(
            "        fingerprint: {{ algorithm: {}, value: {} }},\n",
            ts_string(&fingerprint.algorithm),
            ts_string(&fingerprint.value)
        )),
        None => out.push_str("        fingerprint: null,\n"),
    }
    out.push_str("      },\n");
    out.push_str(&format!("      accepted: {},\n", readout.accepted));
    out.push_str(&format!("      errorCount: {},\n", readout.error_count));
    out.push_str(&format!("      warningCount: {},\n", readout.warning_count));
    out.push_str("      diagnostics: [\n");
    for diagnostic in &readout.diagnostics {
        out.push_str(&format!(
            "        {{ severity: {}, code: {}, path: {}, referenceId: {}, definitionKind: {}, message: {} }},\n",
            ts_string(&diagnostic.severity),
            ts_string(&diagnostic.code),
            ts_string(&diagnostic.path),
            diagnostic.reference_id.as_deref().map(ts_string).unwrap_or_else(|| "null".to_string()),
            diagnostic.definition_kind.as_deref().map(ts_string).unwrap_or_else(|| "null".to_string()),
            ts_string(&diagnostic.message),
        ));
    }
    out.push_str("      ],\n");
    out.push_str("    },\n");
    out
}

fn render_content_validation_catalog(readouts: &[ContentValidationReadout]) -> String {
    let mut out = String::from(
        "export const rustBackedContentValidationCatalog: RulebenchContentValidationCatalogDto = {\n",
    );
    out.push_str("  reports: [\n");
    for readout in readouts {
        out.push_str(&render_content_validation_readout(readout));
    }
    out.push_str("  ],\n");
    out.push_str("};\n");
    out
}

fn render_content_validation_readout(readout: &ContentValidationReadout) -> String {
    let mut out = String::from("    {\n");
    out.push_str(&format!(
        "      scenarioId: {},\n",
        ts_string(&readout.scenario_id)
    ));
    out.push_str(&format!(
        "      scenarioTitle: {},\n",
        ts_string(&readout.scenario_title)
    ));
    out.push_str(&render_content_validation_report(&readout.report, "      "));
    out.push_str("    },\n");
    out
}

fn render_content_validation_report(report: &ContentValidationReport, indent: &str) -> String {
    let nested = format!("{indent}  ");
    let mut out = format!("{indent}report: {{\n");
    out.push_str(&format!("{nested}accepted: {},\n", report.accepted));
    out.push_str(&format!("{nested}errorCount: {},\n", report.error_count));
    out.push_str(&format!(
        "{nested}warningCount: {},\n",
        report.warning_count
    ));
    out.push_str(&format!("{nested}diagnostics: [\n"));
    for diagnostic in &report.diagnostics {
        out.push_str(&render_content_diagnostic(
            diagnostic,
            &format!("{nested}  "),
        ));
    }
    out.push_str(&format!("{nested}],\n"));
    out.push_str(&format!("{indent}}},\n"));
    out
}

fn render_content_diagnostic(diagnostic: &ContentDiagnostic, indent: &str) -> String {
    format!(
        "{indent}{{ severity: {}, code: {}, contentId: {}, message: {} }},\n",
        ts_string(content_diagnostic_severity(diagnostic.severity)),
        ts_string(diagnostic.code.code()),
        match &diagnostic.content_id {
            Some(content_id) => ts_string(content_id),
            None => "null".to_string(),
        },
        ts_string(&diagnostic.message)
    )
}

fn content_diagnostic_severity(severity: ContentDiagnosticSeverity) -> &'static str {
    match severity {
        ContentDiagnosticSeverity::Error => "error",
        ContentDiagnosticSeverity::Warning => "warning",
    }
}

fn render_ruleset_catalog(catalog: &RulesetCatalogReadout) -> String {
    let mut out =
        String::from("export const rustBackedRulesetCatalog: RulebenchRulesetCatalogDto = {\n");
    out.push_str(&format!(
        "  selectedRulesetId: {},\n",
        ts_string(&catalog.selected_ruleset_id)
    ));
    out.push_str("  rulesets: [\n");
    for ruleset in &catalog.rulesets {
        out.push_str(&render_ruleset_summary(ruleset));
    }
    out.push_str("  ],\n");
    out.push_str("};\n");
    out
}

fn render_ruleset_summary(ruleset: &RulesetMetadata) -> String {
    format!(
        "    {{ id: {}, name: {}, version: {}, summary: {} }},\n",
        ts_string(&ruleset.id),
        ts_string(&ruleset.name),
        ts_string(&ruleset.version),
        ts_string(&ruleset.summary)
    )
}

fn render_summary(summary: &ScenarioCatalogSummary) -> String {
    format!(
        "    {{ id: {}, title: {}, summary: {}, seedLabel: {}, outcomeClass: {} }},\n",
        ts_string(&summary.id),
        ts_string(&summary.title),
        ts_string(&summary.summary),
        ts_string(&summary.seed_label),
        ts_string(outcome_class(summary.outcome_class))
    )
}

fn render_readout(
    scenario: &RulebenchScenario,
    events: &[DomainEvent],
    trace: &[TraceEntry],
    final_state: &ScenarioProjection,
    target: &TargetLegality,
) -> String {
    let mut out = String::from("    {\n");
    out.push_str(&format!(
        "      id: {},\n",
        ts_string(&scenario.metadata.id)
    ));
    out.push_str(&format!(
        "      title: {},\n",
        ts_string(&scenario.metadata.title)
    ));
    out.push_str(&format!(
        "      summary: {},\n",
        ts_string(&scenario.metadata.summary)
    ));
    out.push_str(&format!(
        "      seedLabel: {},\n",
        ts_string(&scenario.metadata.seed_label)
    ));
    out.push_str(&render_grid(scenario));
    out.push_str(&render_combatants(scenario, &final_state.combatants));
    out.push_str(&render_action(scenario));
    out.push_str("      selectedTarget: {\n");
    out.push_str(&format!(
        "        targetId: {},\n",
        ts_string(&target.target_id)
    ));
    out.push_str(&format!(
        "        legality: {},\n",
        ts_string(if target.accepted {
            "accepted"
        } else {
            "rejected"
        })
    ));
    out.push_str(&format!("        reason: {},\n", ts_string(&target.reason)));
    out.push_str("      },\n");
    out.push_str("      domainEvents: [\n");
    for (index, event) in events.iter().enumerate() {
        out.push_str(&render_event((index + 1) as u32, event));
    }
    out.push_str("      ],\n");
    out.push_str("      trace: [\n");
    for entry in trace {
        out.push_str("        {\n");
        out.push_str(&format!("          sequence: {},\n", entry.sequence));
        out.push_str(&format!(
            "          phase: {},\n",
            ts_string(phase(entry.phase))
        ));
        out.push_str(&format!(
            "          status: {},\n",
            ts_string(status(entry.status))
        ));
        out.push_str(&format!(
            "          message: {},\n",
            ts_string(&entry.message)
        ));
        out.push_str(&format!(
            "          detail: {},\n",
            ts_string(&entry.detail)
        ));
        out.push_str("        },\n");
    }
    out.push_str("      ],\n");
    out.push_str("      finalState: {\n");
    out.push_str(&format!(
        "        summary: {},\n",
        ts_string(&final_state.summary)
    ));
    out.push_str("        combatants: [\n");
    for combatant in &final_state.combatants {
        out.push_str(&render_final_combatant(combatant));
    }
    out.push_str("        ],\n");
    out.push_str("      },\n");
    out.push_str("    },\n");
    out
}

fn render_grid(scenario: &RulebenchScenario) -> String {
    let mut out = String::from("      grid: {\n");
    out.push_str(&format!("        width: {},\n", scenario.grid.width));
    out.push_str(&format!("        height: {},\n", scenario.grid.height));
    out.push_str("        cells: [\n");
    for cell in &scenario.grid.cells {
        out.push_str(&format!(
            "          {{ x: {}, y: {}, terrainTags: {} }},\n",
            cell.position.x,
            cell.position.y,
            ts_string_array(&cell.terrain_tags)
        ));
    }
    out.push_str("        ],\n");
    out.push_str("      },\n");
    out
}

fn render_combatants(scenario: &RulebenchScenario, final_states: &[FinalCombatantState]) -> String {
    let mut out = String::from("      combatants: [\n");
    for combatant in &scenario.combatants {
        let final_state = final_states
            .iter()
            .find(|state| state.id == combatant.id)
            .expect("catalog final state has combatant");
        out.push_str(&render_combatant(combatant, final_state));
    }
    out.push_str("      ],\n");
    out
}

fn render_combatant(combatant: &Combatant, final_state: &FinalCombatantState) -> String {
    let mut out = String::from("        {\n");
    out.push_str(&format!("          id: {},\n", ts_string(&combatant.id)));
    out.push_str(&format!(
        "          name: {},\n",
        ts_string(&combatant.name)
    ));
    out.push_str(&format!(
        "          team: {},\n",
        ts_string(match combatant.team {
            Team::Ally => "ally",
            Team::Enemy => "enemy",
        })
    ));
    out.push_str(&format!(
        "          sideId: {},\n",
        ts_string(&combatant.side_id)
    ));
    out.push_str(&format!(
        "          position: {{ x: {}, y: {} }},\n",
        combatant.position.x, combatant.position.y
    ));
    out.push_str(&format!(
        "          hitPoints: {{ current: {}, max: {} }},\n",
        final_state.hit_points.current, final_state.hit_points.max
    ));
    out.push_str("          defenses: [\n");
    for defense in &combatant.defenses {
        out.push_str(&format!(
            "            {{ id: {}, label: {}, value: {} }},\n",
            ts_string(&defense.id),
            ts_string(&defense.label),
            defense.value
        ));
    }
    out.push_str("          ],\n");
    out.push_str(&format!(
        "          conditions: {},\n",
        ts_string_array(&final_state.conditions)
    ));
    out.push_str(&format!("          isActor: {},\n", combatant.is_actor));
    out.push_str("        },\n");
    out
}

fn render_action(scenario: &RulebenchScenario) -> String {
    let action = &scenario.selected_action;
    let mut out = String::from("      selectedAction: {\n");
    out.push_str(&format!("        id: {},\n", ts_string(&action.id)));
    out.push_str(&format!("        name: {},\n", ts_string(&action.name)));
    out.push_str(&format!(
        "        actorId: {},\n",
        ts_string(&action.actor_id)
    ));
    out.push_str(&format!(
        "        targetIds: {},\n",
        ts_string_array(&action.targeting.target_ids)
    ));
    out.push_str(&format!(
        "        range: {},\n",
        action.targeting.maximum_range
    ));
    out.push_str(&format!(
        "        lineOfSightRequired: {},\n",
        action.targeting.visibility_requirement == VisibilityRequirement::Required
    ));
    out.push_str(&format!(
        "        visibleTargetIds: {},\n",
        ts_string_array(&action.targeting.visible_target_ids)
    ));
    match &action.check {
        CheckDeclaration::Attack(attack) => {
            out.push_str("        attack: {\n");
            out.push_str(&format!("          modifier: {},\n", attack.modifier));
            out.push_str(&format!(
                "          defenseId: {},\n",
                ts_string(&attack.defense.id)
            ));
            out.push_str(&format!(
                "          defenseLabel: {},\n",
                ts_string(&attack.defense.label)
            ));
            out.push_str("        },\n");
            out.push_str("        savingThrow: null,\n");
            out.push_str("        contested: null,\n");
        }
        CheckDeclaration::SavingThrow(save) => {
            out.push_str("        attack: null,\n");
            out.push_str("        savingThrow: {\n");
            out.push_str(&format!(
                "          saveStatId: {},\n",
                ts_string(&save.save_stat_id)
            ));
            out.push_str(&format!(
                "          difficultyClass: {},\n",
                save.difficulty_class
            ));
            out.push_str("        },\n");
            out.push_str("        contested: null,\n");
        }
        CheckDeclaration::Contested(contested) => {
            out.push_str("        attack: null,\n");
            out.push_str("        savingThrow: null,\n");
            out.push_str("        contested: {\n");
            out.push_str(&format!(
                "          actorStatId: {},\n",
                ts_string(&contested.actor_stat_id)
            ));
            out.push_str(&format!(
                "          targetStatId: {},\n",
                ts_string(&contested.target_stat_id)
            ));
            out.push_str("        },\n");
        }
    }
    out.push_str("        hit: {\n");
    out.push_str(&format!(
        "          damageBonus: {},\n",
        action.hit.damage_bonus
    ));
    out.push_str(&format!(
        "          damageType: {},\n",
        ts_string(&action.hit.damage_type)
    ));
    out.push_str(&format!(
        "          modifierId: {},\n",
        ts_string(&action.hit.modifier_id)
    ));
    out.push_str(&format!(
        "          modifierLabel: {},\n",
        ts_string(&action.hit.modifier_label)
    ));
    out.push_str(&format!(
        "          modifierDuration: {},\n",
        ts_string(&action.hit.modifier_duration)
    ));
    out.push_str("        },\n");
    out.push_str(&format!(
        "        actionText: {},\n",
        ts_string(&action.action_text)
    ));
    out.push_str(&format!(
        "        effectText: {},\n",
        ts_string(&action.effect_text)
    ));
    out.push_str("      },\n");
    out
}

fn render_event(sequence: u32, event: &DomainEvent) -> String {
    match event {
        DomainEvent::ActionUsed {
            actor_id,
            target_id,
            ..
        } => event_block(
            sequence,
            "ActionUsed",
            "Adept used Hexing Bolt against Raider.",
            &[actor_id.as_str(), target_id.as_str()],
        ),
        DomainEvent::AttackRolled {
            actor_id,
            target_id,
            total,
            defense_value,
            outcome,
            ..
        } => {
            let attack_modifier = 4;
            let attack_roll = total - attack_modifier;
            event_block(
                sequence,
                "AttackRolled",
                &format!(
                    "Attack rolled {attack_roll} + {attack_modifier} vs Nerve {defense_value}: {}.",
                    match outcome {
                        AttackOutcome::Hit => "hit",
                        AttackOutcome::Miss => "miss",
                    }
                ),
                &[actor_id.as_str(), target_id.as_str()],
            )
        }
        DomainEvent::SavingThrowResolved {
            actor_id,
            target_id,
            total,
            difficulty_class,
            outcome,
        } => event_block(
            sequence,
            "SavingThrowResolved",
            &format!(
                "Saving throw total {total} against DC {difficulty_class}: {}.",
                match outcome {
                    rulebench_combat::SavingThrowOutcome::Saved => "saved",
                    rulebench_combat::SavingThrowOutcome::Failed => "failed",
                }
            ),
            &[actor_id.as_str(), target_id.as_str()],
        ),
        DomainEvent::ContestedCheckResolved {
            actor_id,
            target_id,
            actor_total,
            target_total,
            outcome,
        } => event_block(
            sequence,
            "ContestedCheckResolved",
            &format!(
                "Contested totals {actor_total} versus {target_total}: {}.",
                match outcome {
                    rulebench_combat::ContestedCheckOutcome::ActorWins => "actor wins",
                    rulebench_combat::ContestedCheckOutcome::TargetWins => "target wins",
                }
            ),
            &[actor_id.as_str(), target_id.as_str()],
        ),
        DomainEvent::DamageApplied {
            target_id, amount, ..
        } => event_block(
            sequence,
            "DamageApplied",
            &format!("Raider took {amount} psychic damage."),
            &[target_id.as_str()],
        ),
        DomainEvent::HealingApplied {
            target_id, amount, ..
        } => event_block(
            sequence,
            "HealingApplied",
            &format!("Raider recovered {amount} vitality."),
            &[target_id.as_str()],
        ),
        DomainEvent::TemporaryVitalityGranted {
            target_id, amount, ..
        } => event_block(
            sequence,
            "TemporaryVitalityGranted",
            &format!("Raider gained {amount} temporary vitality."),
            &[target_id.as_str()],
        ),
        DomainEvent::ModifierApplied { target_id, .. } => event_block(
            sequence,
            "ModifierApplied",
            "Raider became rattled until end of next turn.",
            &[target_id.as_str()],
        ),
        DomainEvent::PositionChanged { actor_id, from, to } => event_block(
            sequence,
            "PositionChanged",
            &format!(
                "{actor_id} moved from {},{} to {},{}.",
                from.x, from.y, to.x, to.y
            ),
            &[actor_id.as_str()],
        ),
        DomainEvent::MovementSpent {
            actor_id,
            amount,
            remaining,
        } => event_block(
            sequence,
            "MovementSpent",
            &format!("{actor_id} spent {amount} movement; {remaining} remains."),
            &[actor_id.as_str()],
        ),
        DomainEvent::EffectMovementApplied {
            target_id,
            movement_kind,
            from,
            to,
        } => event_block(
            sequence,
            "EffectMovementApplied",
            &format!(
                "{target_id} resolved {} movement from {},{} to {},{}.",
                movement_kind.code(),
                from.x,
                from.y,
                to.x,
                to.y
            ),
            &[target_id.as_str()],
        ),
        DomainEvent::ResourceChanged {
            target_id,
            resource_id,
            delta,
            before,
            after,
        } => event_block(
            sequence,
            "ResourceChanged",
            &format!("{target_id} changed {resource_id} by {delta} from {before} to {after}."),
            &[target_id.as_str()],
        ),
        DomainEvent::IntentShapeAccepted { .. } => String::new(),
    }
}

fn event_block(sequence: u32, event_type: &str, summary: &str, entity_ids: &[&str]) -> String {
    let ids = entity_ids
        .iter()
        .map(|id| ts_string(id))
        .collect::<Vec<_>>()
        .join(", ");
    format!(
        "        {{\n          sequence: {sequence},\n          type: {},\n          summary: {},\n          entityIds: [{ids}],\n        }},\n",
        ts_string(event_type),
        ts_string(summary)
    )
}

fn render_final_combatant(combatant: &FinalCombatantState) -> String {
    format!(
        "          {{\n            id: {},\n            name: {},\n            hitPoints: {{ current: {}, max: {} }},\n            conditions: {},\n          }},\n",
        ts_string(&combatant.id),
        ts_string(&combatant.name),
        combatant.hit_points.current,
        combatant.hit_points.max,
        ts_string_array(&combatant.conditions)
    )
}

fn outcome_class(outcome_class: ScenarioOutcomeClass) -> &'static str {
    match outcome_class {
        ScenarioOutcomeClass::AcceptedHit => "acceptedHit",
        ScenarioOutcomeClass::AcceptedMiss => "acceptedMiss",
        ScenarioOutcomeClass::RejectedTargetLegality => "rejectedTargetLegality",
    }
}

fn phase(phase: TracePhase) -> &'static str {
    match phase {
        TracePhase::Proposal => "proposal",
        TracePhase::Validation => "validation",
        TracePhase::Resolution => "resolution",
        TracePhase::Commit => "commit",
    }
}

fn status(status: TraceStatus) -> &'static str {
    match status {
        TraceStatus::Accepted => "accepted",
        TraceStatus::Rejected => "rejected",
        TraceStatus::Info => "info",
    }
}

fn ts_string(value: &str) -> String {
    format!("'{}'", value.replace('\\', "\\\\").replace('\'', "\\'"))
}

fn ts_string_array(values: &[String]) -> String {
    let values = values
        .iter()
        .map(|value| ts_string(value))
        .collect::<Vec<_>>()
        .join(", ");
    format!("[{values}]")
}
